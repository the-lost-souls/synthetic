                mov   P_UpperOffs, bx           ;Save offs of upper coordinate
                mov   si, bx                    ;Load upper coord-offs
                mov   di, bx                    ;Save upper coord-offs

                mov   ax, fs:[si]                  ;Load X1
                mov   bx, fs:[si + 2]              ;Load Y1

