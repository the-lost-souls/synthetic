                mov   ax, fs:[si + 2]
                add   si, 4
                cmp   ax, P_YCounter
                jg    P_Under
                mov   bx, si
                sub   bx, 4
                mov   P_YCounter, ax
P_Under :
                cmp   ax, P_LowestY
                jl    P_Over
                mov   P_LowestY, ax
P_Over :
