;
;Registers changed : ax, bx, cx, dx, bp
;
;Notes : The coordinates are calculated using origo as center.
;----------------------------------------------------------------------------

F3DTo2D       PROC
                push  cx
                push  dx
                cmp   ax, 0
                je   _DoneX

                cwd
                mov   dl, ah                    ;\
                sal   ax, 8

                idiv  cx                        ;dx:ax / cx

_DoneX :
                cmp   bx, 0                     ;Is Y positive?
                je    _DoneY
                xchg  ax, bx
                cwd
                mov   dl, ah
                sal   ax, 8

                idiv  cx
                xchg  ax, bx
_DoneY :

                pop   dx
                pop   cx

                ret                             ;Go home!
F3DTo2D       ENDP

